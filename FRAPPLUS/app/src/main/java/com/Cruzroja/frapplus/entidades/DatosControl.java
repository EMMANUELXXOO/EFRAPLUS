package com.Cruzroja.frapplus.entidades;

public class DatosControl {
    private String ClaveFRAPOrden;
    private String NoAmbulancia;
    private String Operador;
    private String PrestadoresServicio;
    private String MatriculaHelicoptero;

    public String getClaveFRAPOrden() {
        return ClaveFRAPOrden;
    }

    public void setClaveFRAPOrden(String claveFRAPOrden) {
        ClaveFRAPOrden = claveFRAPOrden;
    }

    public String getNoAmbulancia() {
        return NoAmbulancia;
    }

    public void setNoAmbulancia(String noAmbulancia) {
        NoAmbulancia = noAmbulancia;
    }

    public String getOperador() {
        return Operador;
    }

    public void setOperador(String operador) {
        Operador = operador;
    }

    public String getPrestadoresServicio() {
        return PrestadoresServicio;
    }

    public void setPrestadoresServicio(String prestadoresServicio) {
        PrestadoresServicio = prestadoresServicio;
    }

    public String getMatriculaHelicoptero() {
        return MatriculaHelicoptero;
    }

    public void setMatriculaHelicoptero(String matriculaHelicoptero) {
        MatriculaHelicoptero = matriculaHelicoptero;
    }
}

package com.Cruzroja.frapplus.entidades;

public class HospitalReceptor {


    private String ClaveFRAPOrden;
    private String FirmaEntegaPaciente;
    private String NombreEntregaPaciente;
    private String FirmaRecibePaciente;
    private String NombreRecibePaciente;

    public String getClaveFRAPOrden() {
        return ClaveFRAPOrden;
    }

    public void setClaveFRAPOrden(String claveFRAPOrden) {
        ClaveFRAPOrden = claveFRAPOrden;
    }

    public String getFirmaEntegaPaciente() {
        return FirmaEntegaPaciente;
    }

    public void setFirmaEntegaPaciente(String firmaEntegaPaciente) {
        FirmaEntegaPaciente = firmaEntegaPaciente;
    }

    public String getNombreEntregaPaciente() {
        return NombreEntregaPaciente;
    }

    public void setNombreEntregaPaciente(String nombreEntregaPaciente) {
        NombreEntregaPaciente = nombreEntregaPaciente;
    }

    public String getFirmaRecibePaciente() {
        return FirmaRecibePaciente;
    }

    public void setFirmaRecibePaciente(String firmaRecibePaciente) {
        FirmaRecibePaciente = firmaRecibePaciente;
    }

    public String getNombreRecibePaciente() {
        return NombreRecibePaciente;
    }

    public void setNombreRecibePaciente(String nombreRecibePaciente) {
        NombreRecibePaciente = nombreRecibePaciente;
    }
}
